package team2.account_admin;

public class Account_AdminDataBean {

	private int account_admin_num;
	private String bank_name;
	private String account_holder;
	private String account_num;
	
	
	public int getAccount_admin_num() {
		return account_admin_num;
	}
	public void setAccount_admin_num(int account_admin_num) {
		this.account_admin_num = account_admin_num;
	}
	public String getBank_name() {
		return bank_name;
	}
	public void setBank_name(String bank_name) {
		this.bank_name = bank_name;
	}
	public String getAccount_holder() {
		return account_holder;
	}
	public void setAccount_holder(String account_holder) {
		this.account_holder = account_holder;
	}
	public String getAccount_num() {
		return account_num;
	}
	public void setAccount_num(String account_num) {
		this.account_num = account_num;
	}
	
	
}
