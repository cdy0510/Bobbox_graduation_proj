package team2.supplier;

public class SupplierDataBean {
	private int supplier_num;
	private String supplier_name;
	private String supplier_man_name;
	private String supplier_address;
	private String supplier_tel;
	private String supplier_mail;
	private String supplier_bank;
	private String supplier_account_data;
	
	
	public String getSupplier_man_name() {
		return supplier_man_name;
	}
	public void setSupplier_man_name(String supplier_man_name) {
		this.supplier_man_name = supplier_man_name;
	}
	public int getSupplier_num() {
		return supplier_num;
	}
	public void setSupplier_num(int supplier_num) {
		this.supplier_num = supplier_num;
	}
	public String getSupplier_name() {
		return supplier_name;
	}
	public void setSupplier_name(String supplier_name) {
		this.supplier_name = supplier_name;
	}
	public String getSupplier_address() {
		return supplier_address;
	}
	public void setSupplier_address(String supplier_address) {
		this.supplier_address = supplier_address;
	}
	public String getSupplier_tel() {
		return supplier_tel;
	}
	public void setSupplier_tel(String supplier_tel) {
		this.supplier_tel = supplier_tel;
	}
	public String getSupplier_mail() {
		return supplier_mail;
	}
	public void setSupplier_mail(String supplier_mail) {
		this.supplier_mail = supplier_mail;
	}
	public String getSupplier_bank() {
		return supplier_bank;
	}
	public void setSupplier_bank(String supplier_bank) {
		this.supplier_bank = supplier_bank;
	}
	public String getSupplier_account_data() {
		return supplier_account_data;
	}
	public void setSupplier_account_data(String supplier_account_data) {
		this.supplier_account_data = supplier_account_data;
	}
}
